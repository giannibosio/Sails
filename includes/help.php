<p>
						Hangglider sails have two
						<b>sides</b> - top and bottom- and each side is composed by several fabric
						<b>panels</b>.
					</p>
					<p>
						<b>
							The purpose of the configurator is to ease the process of choosing the colors a/o materials for every panel,
							by supplying its appropriate palette of choices
						</b>
						<br />
						An interactive schema is displayed, where the customer can select panels and colors, and visually experiment various combinations
						and eventually keep track of the preferred configuration :
					</p>
					<ul>
						<li>
							by
							<b>printing</b> the configuration summary, or
						</li>
						<li>
							by
							<b>downloading</b> the summary in PDF format
						</li>
					</ul>
					<br />

					<h4>configurator page, where the interaction take place</h4>
					The mouse interaction is simple and natural
					<br />
					<ul>
						<li>click inside one panel area to toggle show-hide its related available-colors palette  </li>
						<li>
							click the color of your choice and get the panel filled in - (and when filled, the palette disappear)
						</li>
						<li>
							repeat this process as you like
						</li>
						<li>
							use the TOP/BOTTOM toggle to switch sail schema
						</li>
						<li>
							when you are done with one side configuration push the blue button to SAVE this side choices
						</li>
						<li>
							repeat and SAVE for the other side too
						</li>
						<li>
							once you saved both side configuration click the green button to CHECK your configuration summary
						</li>
					</ul>
					<h4>summary check page, where the summary is displayed, allowing for printing or downloading </h4>
					<ul>
						<li>
							this page summarize your configuration showing the two sides
							<b>schemas</b> for a visual check
						</li>
						<li>
							and one
							<b>table</b> to keep clearly in writing the details of your configuration
							<li> if something needs to be modified click CLOSE and go back</li>
							<li>
								if it's all ok, than click the red button and open the PRINT PREVIEW panel of the browser
								<ol>
									<li>set the output destination to file.PDF or to the PRINTER</li>
									<li> set the page orientation to LANDSCAPE</li>
									<li>click the PRINT button to get your output.</li>
								</ol>
							</li>
						</ul>
